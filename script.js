//functions for calculator
const add = (number1, number2) => {
    const total = number1 + number2;
    return total;
}

const subtract = (number1, number2) => {
    const total = number1 - number2;
    return total;
}

const multiply = (number1, number2) => {
    const total = number1 * number2;
    return total;
}

const divide = (number1, number2) => {
    const total = number1 / number2;
    return total;
}